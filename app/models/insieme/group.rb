module Insieme::Group
  extend ActiveSupport::Concern

  included do
    # Flag whether this group has reporting or not
    class_attribute :reporting
    self.reporting = false

    root_types Group::Dachverein

    has_many :time_records, dependent: :destroy

    validates :canton, inclusion: {in: Cantons.short_name_strings, allow_blank: true}

    def self.by_bsv_number
      where
        .not(bsv_number: nil)
        .order(Arel.sql("CASE WHEN id = #{Group.root.id} THEN 0 ELSE bsv_number END"))
    end
  end

  def canton
    self[:canton]
  end

  def canton_label
    Cantons.full_name(canton)
  end
end
